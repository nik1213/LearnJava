public class D2_L4_literals {
    public static void main(String[] arr ){
        byte age = 34;
        char ch = 'N';
        float f1= 5.6f;
        double d1 = 5.333D;
        short age3 = 242;
        // we dont need to specify anything at last of the number becaus the entered number is already short
        long age4 = 56567658575L;
        // We have entered L at last of the number above because the entered number is long 
        boolean a = true;
        String N = "Nikhil Ranjan";
        

        System.out.println(a);
        System.out.println(f1);
        System.out.println(ch);
        System.out.println(d1);
        System.out.println(age);
        System.out.println(age3);
        System.out.println(age4);
        System.out.println(N);

    }

}
